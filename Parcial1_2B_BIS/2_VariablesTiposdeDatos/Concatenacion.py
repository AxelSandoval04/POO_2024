# formas de concatenar
nombre="Axel Sandoval"
especial="Area sw de multiplataforma"
carrera="TI"
#1er forma
print("Mi nombre es " + nombre + " mi especialidad es " + especial + " mi carrera es " + carrera)
print("\n")
#2da forma
print("Mi nombre es ", nombre, " mi especialidad es " , especial , " mi carrera es " , carrera)
print("\n")
#3ra forma
print(f"Mi nombre es , {nombre}  mi especialidad es , { especial } , mi carrera es  , {carrera} ")
print("\n")
#4ta forma
print("Mi nombre es , {} ,  mi especialidad es , {} , mi carrera es  , {} ".format (nombre,especial,carrera))
print("\n")
#5ta forma
print('Mi nombre es ' + nombre + ' mi especialidad es ' + especial + ' mi carrera es ' + carrera)
print("\n")